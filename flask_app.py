# app/flask_app.py

from flask import Flask, request, jsonify
import joblib
import pandas as pd

app = Flask(__name__)

# Load the trained model
model = joblib.load('churn_model.pkl')  # Make sure the path is correct

# Define home route
@app.route('/')
def home():
    return "Welcome to the Telecom Customer Churn Prediction API!"

# Define predict route
@app.route('/predict', methods=['POST'])
def predict():
    try:
        # Expect JSON input
        data = request.get_json(force=True)

        # Convert input JSON to pandas DataFrame
        input_data = pd.DataFrame([data])

        # Make prediction
        prediction_proba = model.predict_proba(input_data)[:,1][0]
        prediction = model.predict(input_data)[0]

        # Return prediction and probability
        result = {
            'churn_probability': round(prediction_proba, 3),
            'churn_prediction': int(prediction)  # 1: Will Churn, 0: Will Not Churn
        }
        return jsonify(result)

    except Exception as e:
        return jsonify({'error': str(e)})

if __name__ == '__main__':
    app.run(debug=True)